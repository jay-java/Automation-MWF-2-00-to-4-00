package core;

public class P002_Datatype {
	public static void main(String[] args) {
		byte b = 12;// 1
		System.out.println(b);
		short s = 32767;// 2
		System.out.println("value of short s = "+s);
		int i = 12;// 4
		System.out.println("value of int i = "+i);
		long l = 12;// 8

		float f = 3.14f;// 4
		double d = 3.14;// 8

		char c = 'g';
		boolean bool = false;

	}
}
